#include <stdio.h>
#include <string.h>

#define FLEN 35

static char ciphertext[FLEN] = {0x51,0x1f,0x82,0xa0,0x5d,0x26,0xe1,0x4e,0x72,0xa1,0xb4,0x2d,0x3b,0xcb,0x2f,0x48,0xa1,0x31,0xa3,0x1e,0x32,0xd9,0xf9,0x6f,0x78,0x1c,0x7e,0x16,0x7d,0x16,0xe5,0x8f,0xe1,0x2e,0x9f};
static char key[FLEN]  = {0x19,0x4c,0xc1,0xf4,0x1b,0x5d,0x8f,0x21,0x06,0xfe,0xd3,0x42,0x55,0xa5,0x4e,0x17,0xd2,0x54,0xc6,0x41,0x5f,0xbc,0xa6,0x06,0x16,0x43,0x13,0x73,0x10,0x79,0x97,0xf6,0xda,0x07,0xe2};

int main(void) {
    char input[100];

    printf("Enter the flag: ");
    scanf("%s", input);

    int match = 1;
    for (int i = 0; i < FLEN; i++) {
        if ((input[i] ^ key[i]) != ciphertext[i]) {
            match = 0;
            break;
        }
    }
    if (match) {
        printf("Correct!\n");
    }
    else {
        printf("Incorrect flag...\n");
    }
}
