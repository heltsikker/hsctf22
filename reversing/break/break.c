#include <stdio.h>
#include <string.h>

#define FLEN 24

static char ciphertext[FLEN] = {0xae,0x63,0xb5,0xdc,0x41,0x4c,0xfa,0xe7,0x3f,0x0c,0x0a,0x92,0xdc,0xd5,0xae,0x8b,0x0e,0xe6,0x9e,0x6f,0x66,0xff,0xca,0xd1};
static char key[FLEN]  = {0xe6,0x30,0xf6,0x88,0x07,0x37,0x83,0x88,0x4a,0x53,0x6b,0xe0,0xb9,0x8a,0xcf,0xd4,0x69,0x82,0xfc,0x30,0x01,0x90,0xae,0xac};

int main(void) {
    char flag[FLEN];
    char input[100];
    for (int i = 0; i < FLEN; i++) {
        flag[i] = ciphertext[i] ^ key[i];
    }

    printf("Enter the flag: ");
    scanf("%s", input);
    if (strcmp(flag, input) == 0) {
        printf("Correct!\n");
    }
    else {
        printf("Incorrect flag...\n");
    }
}
